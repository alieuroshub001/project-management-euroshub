import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "antd/dist/reset.css"; // Ant Design v5 reset (recommended)
import "./globals.css";
import Providers from "@/components/Providers/SessionProviderWrapper";
import { getServerSession } from "next-auth";
import { authOptions } from "@/lib/auth";
import AntdCompat from "./antd-compat"; // ðŸ‘ˆ React 19 compatibility patch

const geistSans = Geist({ variable: "--font-geist-sans", subsets: ["latin"] });
const geistMono = Geist_Mono({ variable: "--font-geist-mono", subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{ children: React.ReactNode }>) {
  const session = await getServerSession(authOptions);

  return (
    <html lang="en">
      <body className={`${geistSans.variable} ${geistMono.variable} antialiased`}>
        <AntdCompat /> {/* mounts the React 19 patch once on the client */}
        <Providers session={session}>{children}</Providers>
      </body>
    </html>
  );
}
